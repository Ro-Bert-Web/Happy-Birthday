let points = 20;
for(let i = 0; i < font.length; i++){
	for(let j = font[i].length - 1 ; j > 0; j--){
		let segment = font[i][j].add(font[i][j - 1].mlt(new Complex(1, Math.PI)));
		for(let k = points - 1; k > 0; k--){
			font[i].splice(j, 0, font[i][j - 1].add(segment.mlt(new Complex(k / points, 0))));
		}
	}
}

let form = [];
let text = [];
let cursor = new Complex(0, 0);

function type(x){
	if(x >= 0){
		for(let i = 0; i < font[x].length; i++){
			text.push(font[x][i].add(cursor));
		}
		cursor = cursor.add(new Complex(4, 0));
		if(x == 12 || x == 22){
			cursor = cursor.add(new Complex(2, 0));
		}
	}
	//Space
	else if(x == -1){
		cursor = cursor.add(new Complex(4, 0));
	}
	//Enter
	else if(x == -2){
		for(let i = 0; i < points; i++){
			text.push(cursor.add(new Complex().fromRect(4, 5)));
		}
		for(let i = 0; i < points; i++){
			text.push(new Complex().fromRect(-2, cursor.getY() + 5));
		}

		cursor = new Complex().fromRect(0, cursor.getY() + 6);

		for(let i = 0; i < points; i++){
			text.push(new Complex().fromRect(-2, cursor.getY() + 5));
		}
	}
}

function compile(s = 20, c = -1){
	for(let i = 0; i < points; i++){
		text.push(cursor.add(new Complex().fromRect(4, 5)));
	}
	for(let i = 0; i < points; i++){
		text.push(new Complex().fromRect(-2, cursor.getY() + 5));
	}
	for(let i = 0; i < points; i++){
		text.push(new Complex().fromRect(-2, 5));
	}

	if(c == -1){
		c = text.length / 5;
	}

	form = calculate(text, c, s, true);
}

function spell(t, s= 20, c = -1){
	for(let i = 0; i < t.length; i++){
		type(t[i]);
	}
	compile(s, c);
}

